    private void usersignup(String UserName, String Password) {
        
        java.sql.Connection playlist = Connection.B();
        if(playlist != null){
            try
            {
            s= playlist.prepareStatement("INSERT INTO login(UserName,Password) VALUES(?,?)");
            s.setString(1, UserName);
            s.setString(2, Password);
            s.executeUpdate();
            JOptionPane.showMessageDialog(this, "signup successful!", "MSG",JOptionPane.INFORMATION_MESSAGE);
            uname.setText("");
            pword.setText("");
        }catch(SQLException ex){
        Logger.getLogger(signup.class.getName()).log(Level.SEVERE,null,ex);
    }
        } else
        {
            System.out.println("The connection not available");
        }
    }
